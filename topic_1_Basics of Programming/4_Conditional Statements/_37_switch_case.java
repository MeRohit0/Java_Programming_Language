// Switch Statement are used to match exact thing which is in the variable
public class _37_switch_case {
    public static void main(String []args){
        int number = 3;
        switch(number){
            case 1 : System.out.println("samosa");
                    break;
            case 2 : System.out.println("Burger");
                    break;
            case 3 : System.out.println("Mango Shake");
                    break;
            case 4 : System.out.println("Maggi");
                    break;
            default : System.out.println("Your are dreamming");
                    break;
        }
    }
}
