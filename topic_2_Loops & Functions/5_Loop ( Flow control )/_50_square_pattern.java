// program for print of square patter in console
public class _50_square_pattern {
    public static void main(String args[]){
        // using for loop 
        System.out.println("Square Pattern : ");
        for(int i = 1 ; i <=4 ; i++){
            System.out.println("*   *   *   *");
        }

         System.out.println();
        //done using while loop 
        int i = 1;
        while(i<=4){
            System.out.println("*   *   *   *");
            i++;
        }
    }
}
