/*
do while loop 
it is a exit check loop
its initialization , condition and updation statement all are in different place 
 */

public class _53_do_while_loop {
    public static void main(String[] args) {
        //initialization statement
        int counter = 1;
        do {
            System.out.println("Hello World");
            counter++;
            //updation statement

        }while (counter <= 10 );    // condition check statement 
    } 
}
