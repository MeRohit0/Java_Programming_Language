//Variables in Java 

public class _5_JavaBasics3 {
    public static void main(String args[]){
        int a = 10;
        int b = 5;
        System.out.println(a);
        System.out.println(b);
        String name = "Tony Stark";
        System.out.println(name);

        // Change the value of variables
       
        a = b;
        System.out.println(a);
        
    }    
}
